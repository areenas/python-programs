a= int(input("Enter the number:"))
b= int(input("Enter the number:"))
op=input("Enter the operator: +,-,*,/,%,^ ")
if(op=='+'):
    print("addition",a+b)
elif(op=='-'):
    print("substraction",a-b)
elif(op=='*'):
    print("multiplication",a*b)
elif(op=='/'):
    print("Division",a/b)
elif(op=='%'):
    print("modula",a%b)
elif(op=='^'):
    print("power",a^b)
else:
    print("Ivalid choice")
       


